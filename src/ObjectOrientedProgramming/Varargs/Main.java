package ObjectOrientedProgramming.Varargs;

public class Main {
    public static void main(String[] args){
        System.out.println(Varargs.sum(3));
        System.out.println(Varargs.sum(3,3));
        System.out.println(Varargs.sum(3, 3, 3));
        System.out.println(Varargs.sum(3, 3, 3, 3));
        System.out.println(Varargs.sum(3, 3, 3, 3, 3));
    }
}
